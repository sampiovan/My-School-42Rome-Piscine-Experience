void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	number;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			if (tab[i] < tab[j])
			{
				number = tab[i];
				tab[i] = tab[j];
				tab[j] = number;
			}
			j++;
		}
		i++;
	}
}
