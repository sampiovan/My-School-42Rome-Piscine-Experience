int	ft_str_is_alpha(char *str)
{
	int		i;
	char	lett;

	i = 0;
	if (str[i] == '\0')
		return (1);
	else
	{
		while (str[i] != '\0')
		{
			lett = str[i];
			if (!((lett >= 'A' && lett <= 'Z') || (lett >= 'a' && lett <= 'z')))
				return (0);
			i++;
		}
	}
	return (1);
}
