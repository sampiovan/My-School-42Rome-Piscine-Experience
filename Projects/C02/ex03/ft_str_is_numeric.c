int	ft_str_is_numeric(char *str)
{
	int		i;
	char	num;

	i = 0;
	if (str[i] == '\0')
		return (1);
	else
	{
		while (str[i] != '\0')
		{
			num = str[i];
			if (!(num >= '0' && num <= '9'))
				return (0);
			i++;
		}
	}
	return (1);
}
