int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	j;

	j = 0;
	while (s1[j] != '\0' && s2[j] != '\0' && n > 0)
	{
		if (s1[j] != s2[j])
			break ;
		j++;
		n--;
	}
	if (n == 0)
		return (0);
	return (s1[j] - s2[j]);
}
